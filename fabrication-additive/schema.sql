-- ---------- Table des demandes d'impression --------------------------
create table if not exists public.demandes (
  id                  bigint generated by default as identity primary key,
  created_at          timestamptz not null default now(),
  etudiant_nom        text not null default '',
  etudiant_prenom     text not null default '',
  projet              text not null default '',
  fichier_nom         text not null default '',
  drive_file_id       text default '',
  drive_link          text default '',
  machine             text not null default 'bambulab',   -- bambulab | markforged
  volume_mm3          double precision default 0,
  bbox                text default '',                      -- "LxLxH mm"
  temps_estime_min    integer default 0,
  temps_final_min     integer default 0,
  statut              text not null default 'en_attente',  -- en_attente|validee|refusee|en_cours|imprimee
  encadrant_nom       text default '',
  encadrant_valide_at timestamptz,
  imprime_at          timestamptz,
  notes               text default '',                   -- commentaire de l'étudiant
  numero              integer default 0,                 -- n° incrémental par projet
  taraudage           boolean default false,
  insert_filete       boolean default false,
  usinage             boolean default false,
  finition            text default 'brouillon',          -- brouillon | finition
  temps_categorie     text default '',                   -- "moins de 3h" ...
  encadrant_commentaire text default ''
);

-- Colonnes ajoutées après coup (pour une base déjà créée : relançable sans risque)
alter table public.demandes add column if not exists numero               integer default 0;
alter table public.demandes add column if not exists taraudage            boolean default false;
alter table public.demandes add column if not exists insert_filete        boolean default false;
alter table public.demandes add column if not exists usinage              boolean default false;
alter table public.demandes add column if not exists finition             text default 'brouillon';
alter table public.demandes add column if not exists temps_categorie      text default '';
alter table public.demandes add column if not exists encadrant_commentaire text default '';
alter table public.demandes add column if not exists en_cours_at          timestamptz;
alter table public.demandes add column if not exists duree_reelle_min     integer default 0;
alter table public.demandes add column if not exists operateur_nom        text default '';
alter table public.demandes add column if not exists archive              boolean default false;  -- demande archivée (retirée du tableau opérateur)
alter table public.demandes add column if not exists priorite             integer default 0;      -- ordre dans la file d'attente d'impression
alter table public.demandes add column if not exists photo_file_id        text default '';        -- ID Drive de la photo de la pièce terminée
alter table public.demandes add column if not exists photo_link           text default '';        -- lien Drive de la photo de la pièce terminée

-- Index pour le suivi étudiant (recherche par nom/prénom) et les listes
create index if not exists demandes_statut_idx  on public.demandes (statut);
create index if not exists demandes_etudiant_idx on public.demandes (lower(etudiant_nom), lower(etudiant_prenom));

-- Colonnes ajoutées à la table etudiants (gestion depuis la page d'accueil) :
-- formation de l'étudiant (2A, 2I, 3A...) et 3e encadrant possible.
alter table public.etudiants add column if not exists formation  text default '';
alter table public.etudiants add column if not exists encadrant3 text default '';
create index if not exists demandes_projet_idx   on public.demandes (projet);

-- Colonne ajoutée à la table bookings (Réservation Machines / usinage) : le 3e encadrant
-- d'un projet est désormais aussi affiché/rempli automatiquement lors d'une réservation.
alter table public.bookings add column if not exists encadrant3 text default '';

-- ---------- Table des paramètres (clé / valeur) ----------------------
create table if not exists public.parametres (
  cle    text primary key,
  valeur text not null default ''
);

-- Valeurs par défaut (ne réécrase pas si déjà présentes)
